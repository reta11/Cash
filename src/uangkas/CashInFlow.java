    public HashMap<String, String> populateCombobox(){
        HashMap<String, String> map = new HashMap<String, String>();
        
        try{
            statCash = con.createStatement();
            rs = statCash.executeQuery("SELECT `kd_member`, `name` FROM member WHERE id_bendahara='"+id_admin+"'");
            comboboxItem cmi;
            
            while(rs.next()){
                cmi = new comboboxItem(rs.getString(1), rs.getString(2));
                map.put(cmi.getMemberName(), cmi.getMemberId());
//                jComboBox1.addItem(cmi.getMemberName());
            }
        }catch(Exception exc){
            System.err.println(exc.getMessage());
        }
        
        return map;
        
    }
    
