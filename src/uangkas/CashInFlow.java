    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
//            koneksi();
            int row =jTable1.getSelectedRow();
            String tabel_klik=(jTable1.getModel().getValueAt(row, 0).toString());
            java.sql.Statement stm = con.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("SELECT member.name, income.amount, income.date FROM income INNER JOIN member ON income.kd_member=member.kd_member WHERE income.kd_member='"+tabel_klik+"'");
//            java.sql.ResultSet sql = stm.executeQuery("SELECT * FROM income where kd_member='"+tabel_klik+"'");
            if(sql.next()){
                String name = sql.getString("name");
                jComboBox1.setSelectedItem(name);
                String amount = sql.getString("amount");
                inAmount.setText(amount);
                String date = sql.getString("date");
                inDate.setText(date);
            }
        } catch (Exception e) {}
    }//GEN-LAST:event_jTable1MouseClicked

