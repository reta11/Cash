    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(inAmount.getText().equals("") || inDate.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please fill in all data!");
        }else{
            try{
//                koneksi();
                statCash = con.createStatement();
                HashMap<String, String> map = populateCombobox();
                String SQL = "INSERT INTO income(kd_member, amount, date) VALUES('"+map.get(jComboBox1.getSelectedItem().toString())+"','"+inAmount.getText()+"','"+inDate.getText()+"')";
                statCash.executeUpdate(SQL);
                display();
                statCash.close();
//                con.close();
                reset();
                JOptionPane.showMessageDialog(null, "Your data has been saved!");

            }catch(Exception exc){
                System.err.println(exc.getMessage());
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

